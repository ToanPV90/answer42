package com.samjdtechnologies.answer42.model.enums;

/**
 * Enum representing the different types of AI-generated paper analyses.
 */
public enum AnalysisType {
    /**
     * A comprehensive summary of the entire paper.
     */
    DEEP_SUMMARY,
    
    /**
     * Analysis of the methodology used in the paper.
     */
    METHODOLOGY_ANALYSIS,
    
    /**
     * Interpretation of the results presented in the paper.
     */
    RESULTS_INTERPRETATION,
    
    /**
     * Critical evaluation of the paper's strengths and weaknesses.
     */
    CRITICAL_EVALUATION,
    
    /**
     * Discussion of the broader research implications of the paper.
     */
    RESEARCH_IMPLICATIONS,
    
    /**
     * General analysis not falling into the other categories.
     */
    GENERAL
}
